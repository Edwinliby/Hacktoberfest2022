public class Lower_Uppercase {

    public static void main(String args[]){
        String s1="hello welcome to hacktoberfest 2022 "; //entered string
        String s1upper=s1.toUpperCase(); //convertor
        System.out.println("Your entered : "+s1+ '\n' ); //print input string
        System.out.println("After converted to upper case : "+'\n' +s1upper);//print converted string
    }
}

//Sample output for the demostration
//Your entered : hello welcome to hacktoberfest 2022 

//After converted to upper case : 
//HELLO WELCOME TO HACKTOBERFEST 2022 